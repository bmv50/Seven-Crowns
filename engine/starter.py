# -*- coding: utf-8 -*-
"""Стартовый набор расходников новичка — ЕДИНЫЙ ИСТОЧНИК ПРАВДЫ.

Один и тот же список выдаётся при создании персонажа (bot/main.py), заряжается
в модель симулятора онбординга (sim_onboarding.py) и проверяется тестами
(test_onboarding.py). Меняя набор ЗДЕСЬ, мы одновременно двигаем продакшн-выдачу,
поведение симулятора и тест — рассинхрона нет.

Балансировка первой сессии (Этап 4.1): хрупким классам (низкий hp_base, нулевой
damage_reduction, зависимость от маны) стартовый набор усилен зельями. Обоснование
и цифры до/после — в docs/BALANCE_ONBOARDING.md. Рычаги (а)/(в) регламента правятся
ТОЛЬКО в словаре _CLASS_KIT ниже; цены зелий и мобы не трогаются.
"""
from typing import List

HP_POTION = "малое_зелье"       # 🧪 лечение (+35 HP × HP_SCALE)
MANA_POTION = "зелье_маны"      # 💙 мана (+40 MP, без HP_SCALE)

# Базовая «подушка» новичка — 4 малых зелья лечения. Исторический набор, проверен
# sim --naive для воина ещё на Этапе онбординга. Классы вне _CLASS_KIT получают её.
_BASE_HP_POTIONS = 4

# Классовые наборы: cls -> (число зелий лечения, число зелий маны).
# Рычаг (а) — +зелья лечения хрупким; рычаг (в) — +зелья маны ресурсозависимым.
# ПУСТО на baseline: все классы получают базовые 4 лечения / 0 маны. Заполняется
# рычагами ТОЛЬКО если прогоны симулятора покажут провал целей (см. отчёт).
_CLASS_KIT = {
    # cls: (hp_potions, mana_potions)  — заполняется по итогам baseline-прогонов.
    # Рычаг (а)+(в) Этапа 4.1: хрупким/безлекарным-в-naive классам +зелья лечения.
    # naive-профиль тратит ТОЛЬКО стартовый набор (без докупки) — лишние зелья
    # прямо продлевают «подушку выживания» на уровнях 7–9, где щита уже нет.
    "mage":        (12, 0),
    "necromancer": (12, 0),
    "priest":      (6, 0),
    "paladin":     (12, 0),
}


def starting_consumables(cls: str) -> List[str]:
    """Список ключей расходников стартового набора класса `cls`."""
    hp, mana = _CLASS_KIT.get(cls, (_BASE_HP_POTIONS, 0))
    return [HP_POTION] * hp + [MANA_POTION] * mana


def hp_potion_count(cls: str) -> int:
    """Сколько зелий лечения в стартовом наборе класса (ресток smart-профиля)."""
    return _CLASS_KIT.get(cls, (_BASE_HP_POTIONS, 0))[0]


def mana_potion_count(cls: str) -> int:
    """Сколько зелий маны в стартовом наборе класса."""
    return _CLASS_KIT.get(cls, (_BASE_HP_POTIONS, 0))[1]
