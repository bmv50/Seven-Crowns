# -*- coding: utf-8 -*-
"""
Безопасность пользовательского текста (UGC) перед вставкой в сообщения Telegram.

Бот шлёт сообщения с parse_mode="Markdown" (классический Markdown Telegram).
Если имя персонажа/гильдии или реплика игрока содержит спецсимволы разметки
(* _ ` [), сообщение может «поехать» или вовсе не отправиться (BadRequest).
Плюс злоупотребление невидимыми/управляющими символами в именах — спуфинг и
замусоривание списков игроков.

Здесь:
  • esc_md(s)     — экранирование спецсимволов классического Markdown;
  • clean_name(s) — нормализация/валидация имени (персонаж, гильдия): str|None;
  • clean_chat(s) — чистка реплики чата (убрать управляющие, обрезать длину).

Модуль в engine/ и НЕ зависит от aiogram — тестируется без Telegram.
"""
import unicodedata

# Спецсимволы классического Markdown Telegram, ломающие разметку в тексте UGC.
_MD_SPECIAL = set("*_`[")

# Блок-список подстрок для имён (Этап 7.2). Две группы:
#  • имперсонация служебных ролей (admin/moderator/system/бот/поддержка) —
#    имя не должно выдавать игрока за администрацию или системное сообщение;
#  • мат-минимум (корни) — грубая фильтрация оскорбительных имён на бете.
# Сверка — по нормализованному (lower) имени со схлопнутыми пробелами. Корни
# подобраны так, чтобы минимизировать ложные срабатывания на обычных именах.
_NAME_BLOCK = (
    # служебные роли / имперсонация
    "admin", "админ", "administrator", "moderator", "модератор", "модер",
    "system", "систем", "support", "поддержк", "бот ", " бот", "official",
    # мат-корни (минимум)
    "хуй", "хуё", "пизд", "ебан", "еблан", "выеб", "бляд", "мудак",
    "пидор", "пидар", "гандон", "залуп", "ублюд",
)


def name_reason(s) -> str:
    """Причина отклонения имени (или '' если имя валидно). Используется там, где
    игроку/админу полезно показать, ПОЧЕМУ имя не принято. clean_name возвращает
    None по любой из этих причин — здесь она формулируется человекочитаемо."""
    if not s:
        return "пустое имя"
    raw = _strip_invisible(str(s))
    raw = " ".join(raw.split())
    if not raw:
        return "пустое имя"
    if len(raw) < 2 or len(raw) > 20:
        return "длина должна быть 2–20 символов"
    if raw[0] in "@/":
        return "нельзя начинать с @ или /"
    if not any(c.isalnum() for c in raw):
        return "нужна хотя бы одна буква или цифра"
    low = raw.lower()
    for bad in _NAME_BLOCK:
        if bad in low:
            return "имя содержит запрещённое слово"
    return ""


def esc_md(s) -> str:
    """Экранировать спецсимволы классического Markdown (* _ ` [) обратным
    слэшем — чтобы имя/реплика игрока не ломали разметку и не «съедали» текст."""
    if not s:
        return ""
    out = []
    for ch in str(s):
        if ch in _MD_SPECIAL:
            out.append("\\")
        out.append(ch)
    return "".join(out)


def _strip_invisible(s: str) -> str:
    """Убрать управляющие и форматирующие символы (категории Cc/Cf: перевод
    строки, zero-width, RTL-оверрайды и т.п.)."""
    return "".join(c for c in s if unicodedata.category(c) not in ("Cc", "Cf"))


def clean_name(s, min_len: int = 2, max_len: int = 20):
    """Нормализовать и проверить имя персонажа/гильдии.

    Возвращает очищенное имя или None, если оно недопустимо.
    Правила:
      • обрезка пробелов по краям и схлопывание повторных пробелов;
      • запрет управляющих/невидимых символов (Cc/Cf, zero-width);
      • длина в диапазоне [min_len, max_len];
      • не может состоять только из пунктуации/символов (нужен буквенно-цифровой);
      • не может начинаться с '@' или '/' (маскировка под меншен/команду);
      • не содержит запрещённых подстрок (_NAME_BLOCK: имперсонация ролей/мат).
    """
    if not s:
        return None
    s = _strip_invisible(str(s))
    s = " ".join(s.split())          # trim + схлоп внутренних пробелов
    if not s:
        return None
    if len(s) < min_len or len(s) > max_len:
        return None
    if s[0] in "@/":
        return None
    if not any(c.isalnum() for c in s):
        return None
    low = s.lower()
    if any(bad in low for bad in _NAME_BLOCK):
        return None
    return s


def clean_chat(s, limit: int = 300) -> str:
    """Очистить реплику чата: вырезать управляющие/невидимые символы, схлопнуть
    пробелы, обрезать до limit. Возвращает строку (возможно пустую)."""
    if not s:
        return ""
    s = _strip_invisible(str(s))
    s = " ".join(s.split())
    return s[:limit]
