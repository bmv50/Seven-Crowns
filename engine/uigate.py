# -*- coding: utf-8 -*-
"""
Прогрессивный UI (Этап 4.2, задача 3): скрывает/блокирует кнопки фич,
которые новичку рано трогать (аукцион, гильдии, арена, сезон, профессии,
крафт, ежедневки, таланты, группа, территориальные войны). Цель — не
перегружать интерфейс новичка десятком незнакомых кнопок с первой минуты.

Чистый модуль без Telegram/БД — только числа и строки, легко тестируется.
Используется в bot/ui.py (сборка клавиатур) и bot/main.py (guard в
обработчиках колбэков — на случай, если игрок дожал старую/кэшированную
кнопку или ввёл команду текстом раньше открытия фичи).
"""

# фича → минимальный уровень открытия. Бой/журнал/карта/сумка не гейтятся —
# их тут нет: они доступны с 1 уровня.
FEATURES = {
    "talents": 3,
    "party": 3,
    "professions": 6,
    "craft": 6,
    "daily": 6,
    "auction": 10,
    "factions": 10,     # территориальные войны (текстовая команда "территория")
    "guild": 10,
    "arena": 10,
    "season": 10,
}

# сколько уровней ДО открытия фича уже показывается заблокированной (с 🔒),
# чтобы игрок видел цель прогрессии, но не раньше времени.
GATE_PREVIEW = 4


def unlocked(feature: str, level: int) -> bool:
    """Открыта ли фича персонажу данного уровня. Неизвестная фича — не гейтится
    (считаем открытой), чтобы опечатка в имени фичи не ломала UI молча."""
    min_level = FEATURES.get(feature)
    if min_level is None:
        return True
    return level >= min_level


def hint(feature: str) -> str:
    """Текст всплывашки для запертой фичи."""
    min_level = FEATURES.get(feature)
    if min_level is None:
        return "🔒 Пока недоступно."
    return f"🔒 Откроется на {min_level} ур."


def next_unlock_visible(feature: str, level: int) -> bool:
    """Показывать ли фичу заблокированной кнопкой (а не молча прятать).
    Правило: видна с 🔒, если до открытия осталось ≤ GATE_PREVIEW уровней
    (и фича ещё не открыта — иначе это уже обычная кнопка)."""
    min_level = FEATURES.get(feature)
    if min_level is None:
        return False
    if level >= min_level:
        return False
    return (min_level - level) <= GATE_PREVIEW
