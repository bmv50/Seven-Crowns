def render_command_list() -> str:
    L = ["📜 *Список команд*", ""]
    for cat, cmds in COMMAND_GROUPS:
        L.append(f"*{cat}*")
        for canon, syns, arg, desc in cmds:
            a = f" {arg}" if arg else ""
            syn = f"  _(или: {', '.join(syns)})_" if syns else ""
            L.append(f"`/{canon}{a}` — {desc}{syn}")
        L.append("")
    return "\n".join(L).strip()
